"" + 1 + 0      // "10" (конкатенация строк)
"" - 1 + 0      // -1 (пустая строка преобразуется в 0)
true + false    // 1 (true=1, false=0)
6 / "3"         // 2 (строку "3" преобразует в число)
"2" * "3"       // 6 (оба операнда преобразуются в числа)
4 + 5 + "px"    // "9px" (сначала 4+5=9, затем конкатенация)
"$" + 4 + 5     // "$45" (конкатенация строк)
"4" - 2         // 2 (строку "4" преобразует в число)
"4px" - 2       // NaN (нельзя преобразовать "4px" в число)
" -9 " + 5      // " -9 5" (конкатенация строк)
" -9 " - 5      // -14 (строку " -9 " преобразует в число -9)
null + 1        // 1 (null преобразуется в 0)
undefined + 1   // NaN (undefined преобразуется в NaN)
" \t \n" - 2    // -2 (пустые пробельные символы преобразуются в 0)