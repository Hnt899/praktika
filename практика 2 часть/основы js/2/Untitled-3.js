const birthday = '18.04.1982'; // строчные буквы - значение известно до выполнения кода
const age = someCode(birthday); // строчные буквы - значение вычисляется во время выполнения

// Ответ на вопрос:
// 1. birthday можно записать заглавными (BIRTHDAY), так как это "жёстко закодированное" значение
// 2. age НЕ следует записывать заглавными, так как это вычисляемое значение
// 3. Одновременно для обеих заглавные использовать не стоит - только для "известных заранее" значений

// Правильный вариант:
const BIRTHDAY = '18.04.1982'; // можно заглавные - значение известно заранее
const age = someCode(BIRTHDAY); // строчные - вычисляется во время выполнения